# Imprimir números pares del 2 al 30 usando while
contador = 2  # Inicializamos la variable contadora en 2 (primer número par)

print("NÚMEROS PARES DEL 2 AL 30")
print("=" * 30)

while contador <= 30:
    print(contador)
    contador = contador + 2  # Incrementamos de 2 en 2

print("=" * 30)
print("¡Lista completada!")